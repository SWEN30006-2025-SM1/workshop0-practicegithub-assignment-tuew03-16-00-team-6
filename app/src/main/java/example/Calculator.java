package example;

public class Calculator {
    public int add(int a, int b) {
        int ans;
        ans = a + b;
        return ans;
    }

    public int minus(int a, int b) {
        int ans;
        ans = a - b;
        return ans;
    }

    public int multiply(int a, int b) {
        int ans;
        ans = a * b;
        return ans;
    }

    public double divide(int a, int b) {
        int ans;
        ans = a / b;
        return ans;
    }
}
